    <script>
    document.addEventListener('DOMContentLoaded', function() {
        // Tab switching functionality
        const tabButtons = document.querySelectorAll('.tab-btn');
        const tendersContent = document.getElementById('tenders-content');
        const contractsContent = document.getElementById('contracts-content');

        tabButtons.forEach(button => {
            button.addEventListener('click', function() {
                // Remove active class from all buttons
                tabButtons.forEach(btn => btn.classList.remove('active'));

                // Add active class to clicked button
                this.classList.add('active');

                // Show/hide content based on selected tab
                const tabType = this.getAttribute('data-tab');

                if (tabType === 'tenders') {
                    tendersContent.style.display = 'block';
                    contractsContent.style.display = 'none';
                } else {
                    tendersContent.style.display = 'none';
                    contractsContent.style.display = 'block';
                }
            });
        });

        // Pagination functionality
        const pageButtons = document.querySelectorAll('.page-btn');
        pageButtons.forEach(btn => {
            btn.addEventListener('click', function() {
                if (!this.textContent.includes('❮') && !this.textContent.includes('❯')) {
                    // Remove active class from all page buttons
                    pageButtons.forEach(b => b.classList.remove('active'));

                    // Add active class to clicked button
                    this.classList.add('active');

                    console.log('Page selected:', this.textContent);
                } else {
                    // Handle navigation arrows
                    const currentActive = document.querySelector('.page-btn.active');
                    const currentPage = parseInt(currentActive.textContent);

                    if (this.textContent.includes('❮') && currentPage > 1) {
                        // Previous page
                        currentActive.classList.remove('active');
                        currentActive.previousElementSibling.classList.add('active');
                    } else if (this.textContent.includes('❯') && currentPage < 3) {
                        // Next page
                        currentActive.classList.remove('active');
                        currentActive.nextElementSibling.classList.add('active');
                    }
                }
            });
        });

        // Download button functionality
        const downloadButtons = document.querySelectorAll('.download-btn');
        downloadButtons.forEach(btn => {
            btn.addEventListener('click', function() {
                const title = this.parentElement.querySelector('.tender-title').textContent;

                // Create a simple download simulation
                this.textContent = 'Downloading...';
                this.disabled = true;

                setTimeout(() => {
                    this.textContent = 'Downloaded ✓';
                    this.style.backgroundColor = '#28a745';

                    setTimeout(() => {
                        this.textContent = 'Download';
                        this.disabled = false;
                        this.style.backgroundColor = '#28a745';
                    }, 2000);
                }, 1500);

                console.log('Download initiated for:', title);
            });
        });

        // Tender item hover effects
        const tenderItems = document.querySelectorAll('.tender-item');
        tenderItems.forEach(item => {
            item.addEventListener('mouseenter', function() {
                this.style.backgroundColor = '#f1f3f4';
            });

            item.addEventListener('mouseleave', function() {
                this.style.backgroundColor = '#f8f9fa';
            });
        });
    });
